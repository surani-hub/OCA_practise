class Casting{
	public static void main(String[] args){
		int x = (int)3872.8927;
		
		System.out.println(x);
		
		long l = 17l;
		byte b = (byte) l;
		
		System.out.println(b);
		
		long l1 = 128l;
		byte b1 = (byte)l1;
		
		System.out.println(b1);
		
		System.out.println("*****************************************************************");
		
		
	}
}